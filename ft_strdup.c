#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		i;
	char	*res;

	i = 0;
	res = (char*)ft_memalloc(sizeof(char) * ft_strlen(s1));
	while (s1[i] != '\0')
	{
		res[i] = s1[i];
		i++;
	}
	res[i] = '\0';
	return (res);
}
